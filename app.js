const calculator = {
  add: function(a, b) {
    console.log(a + b);
  },
  minus: function(a, b) {
    console.log(a - b);
  },
  times: function(a, b) {
    console.log(a * b);
  },
  divide: function(a, b) {
    console.log(a / b);
  },
  power: function(a, b) {
    console.log(a ** b);
  },
}

calculator.add(2, 4);
calculator.minus(6, 3);
calculator.times(10, 5);
calculator.divide(100, 24);
calculator.power(2, 2);
